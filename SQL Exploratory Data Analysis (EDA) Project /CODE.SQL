--#5 Magnitude Analysis:

-- Find total customers by countries
SELECT country,COUNT(customer_key) AS total_customers
FROM gold.dim_customers
GROUP BY country
ORDER BY total_customers DESC

-- Find total customers by gender
SELECT gender,COUNT(customer_key) AS total_customers
FROM gold.dim_customers
GROUP BY gender
ORDER BY total_customers DESC

-- Find total products by categoey
SELECT category,COUNT(product_id) AS Products
FROM gold.dim_products
GROUP BY category
ORDER BY Products DESC

-- What is the average costs in each categorey ?
SELECT category,Avg(cost) avg_cost
FROM gold.dim_products
GROUP BY category
ORDER BY avg_cost DESC

-- What is the total revenue generated for each category ?
SELECT p.category,SUM(f.sales_amount) As total_revenue
FROM gold.fact_sales AS f
Left Join gold.dim_products AS p
ON f.product_key=p.product_key
GROUP BY p.category
ORDER BY total_revenue DESC

-- Find total revenue is generated by each customer
SELECT c.customer_key,c.first_name,
c.last_name,SUM (f.sales_amount) As total_revenue
FROM gold.fact_sales AS f
Left Join gold.dim_customers AS c
ON f.customer_key=c.customer_key
GROUP BY c.customer_key,c.first_name,
c.last_name
ORDER BY total_revenue DESC

-- What is the distribution sold items across countries?
SELECT c.country, SUM(f.quantity) AS total_sold_item
FROM gold.fact_sales AS f
Left Join gold.dim_customers AS c
ON f.customer_key=c.customer_key
GROUP BY c.country
ORDER BY total_sold_item DESC


-- #6 Ranking Analysis

-- Which five products generate the Highest revenue?

SELECT Top 5(p.product_name),SUM(f.sales_amount) As total_revenue
FROM gold.fact_sales AS f
Left Join gold.dim_products AS p
ON f.product_key=p.product_key
GROUP BY p.product_name
ORDER BY total_revenue DESC

SELECT * 
FROM (
	SELECT p.product_name,
	SUM(f.sales_amount) As total_revenue,
	Rank()OVER(ORDER BY SUM(f.sales_amount)DESC) as rank_products
	FROM gold.fact_sales AS f
	Left Join gold.dim_products AS p
	ON f.product_key=p.product_key
	GROUP BY p.product_name)t
WHERE rank_products <=5



-- What are the 5 worst performing products in terms of sales?

SELECT Top 5 p.product_name,SUM(f.sales_amount) As total_revenue
FROM gold.fact_sales AS f
Left Join gold.dim_products AS p
ON f.product_key=p.product_key
GROUP BY p.product_name
ORDER BY total_revenue ASC

-- Find the top 10 customers who have generated the highest revenue
SELECT *
FROM (
SELECT c.customer_key,c.first_name,
c.last_name,SUM (f.sales_amount) As total_revenue,
RANK() OVER (ORDER BY SUM (f.sales_amount) DESC ) AS revenue_order
FROM gold.fact_sales AS f
Left Join gold.dim_customers AS c
ON f.customer_key=c.customer_key
GROUP BY c.customer_key,c.first_name,
c.last_name)t
WHERE revenue_order <=10


